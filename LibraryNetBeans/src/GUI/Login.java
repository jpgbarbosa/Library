    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String userName, password;

        userName = jTextField1.getText();
        password = jPasswordField1.getText();

        if (Validation.isInteger(userName)){
            int value = Constants.dbhandler.login(Integer.parseInt(userName), password);

            if (value >= 0){
                Constants.loginMenu.setVisible(false);
                Constants.mainMenu.setVisible(true);
                Constants.frame.setContentPane(Constants.mainMenu);
                Constants.loginNumber = Integer.parseInt(userName);
            }
            else{
                JOptionPane.showMessageDialog(this, "Invalid login...");
            }
        }
        else{
            JOptionPane.showMessageDialog(this, "Invalid username...");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

    public JLabel CreateImage(String path, String toolTip,int x,int y,int x1,int y1){
        BufferedImage img = null;
        Icon icon;
        JLabel label;

        try {
                img = ImageIO.read(new File(path));
        } catch (IOException e){
                System.out.println("Could not open the image located in " + path + "!");
        }
        icon = new ImageIcon(img);
        label = new JLabel(icon);
        label.setBounds(new Rectangle(x,y,x1,y1));
        if (!toolTip.equals(""))
        	label.setToolTipText(toolTip);
        add(label);

        return label;
    }

